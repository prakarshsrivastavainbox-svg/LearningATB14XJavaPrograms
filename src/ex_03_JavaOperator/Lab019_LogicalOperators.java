package ex_03_JavaOperator;

public class Lab019_LogicalOperators {
    public static void main(String[] args) {
        boolean a,b,c,d;
        a=true;b=true;
        c=false;d=false;
        System.out.println(!a);
        System.out.println(!!b);
        System.out.println(a||c);
        System.out.println(a&&d);;
    }
}
