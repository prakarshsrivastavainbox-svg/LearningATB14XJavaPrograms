package ex_03_JavaOperator;

public class Lab017_ArithmeticOperators {
    public static void main(String[] args) {
        int a ,b; float c = 2.11f;
        a=10 ; b=11;

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a+c);
        System.out.println(a-c);
        System.out.println("a+b");
        System.out.println(a+b);
    }
}
